// ═══════════════════════════════════════════════════════════════
// MAIN DISPATCHER
// ═══════════════════════════════════════════════════════════════
export function generateDeviceConfig(device, design, ipAllocs, links) {
  const ips = ipAllocs[device.name] || {};
  const [b1, b2] = parseBase(design?.ip_scheme);
  const vendor = detectVendor(device, design);

  if (vendor === "paloalto") return generatePaloAltoConfig(device, design, ips, links, b1, b2);
  if (vendor === "juniper") return generateJuniperConfig(device, design, ips, links);
  if (vendor === "fortinet") return generateFortinetConfig(device, design, ips, links, b1, b2);
  return generateCiscoConfig(device, design, ips, links, b1, b2);
}

// ── File extension by vendor ──────────────────────────────────────────────────
export function configFileExtension(device, design) {
  const vendor = detectVendor(device, design);
  if (vendor === "paloalto") return ".set";   // PAN-OS set commands
  if (vendor === "juniper") return ".conf";   // JunOS
  if (device.type === "server") return ".sh"; // Shell script
  return ".cfg";                              // Cisco IOS
}