/**
 * Device configuration generators for multiple vendors.
 * Supports: Cisco IOS, Juniper JunOS, Palo Alto PAN-OS
 * Includes CIS/STIG-aligned security hardening best practices.
 */

export function parseBase(ipScheme) {
  const m = (ipScheme || "10.0.0.0/8").match(/(\d+)\.(\d+)\.(\d+)\.(\d+)/);
  return m ? [+m[1], +m[2], +m[3], +m[4]] : [10, 0, 0, 0];
}

// ── Detect vendor from design fields ─────────────────────────────────────────
export function detectVendor(device, design) {
  const fw = (design?.firewall_vendor || "").toLowerCase();
  const router = (design?.router_model || "").toLowerCase();
  const sw = (design?.switch_model || "").toLowerCase();

  if (device.type === "firewall") {
    if (fw.includes("palo alto")) return "paloalto";
    if (fw.includes("juniper") || fw.includes("srx")) return "juniper";
    if (fw.includes("fortinet") || fw.includes("fortigate")) return "fortinet";
    return "cisco"; // Cisco ASA / pfSense
  }
  if (device.type === "router") {
    if (router.includes("juniper") || router.includes("vmx")) return "juniper";
    return "cisco";
  }
  if (device.type === "switch") {
    if (sw.includes("juniper") || sw.includes("ex")) return "juniper";
    if (sw.includes("arista")) return "arista"; // EOS is Cisco-like, handled as cisco
    return "cisco";
  }
  return "cisco";
}

